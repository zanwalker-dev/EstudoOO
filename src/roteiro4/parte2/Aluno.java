package roteiro4.parte2;

public class Aluno {

    public int matricula;
    public String nome;
    public String curso;
    public int anoIngresso;

    Aluno(int pMatricula, String pNome, String pCurso, int pAnoIngresso) {
        matricula = pMatricula;
        nome = pNome;
        curso = pCurso;
        anoIngresso = pAnoIngresso;
    }

}
